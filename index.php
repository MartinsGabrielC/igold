            </div>

            <ul class="list-group">
              <li class="list-group-item">
                <strong>1. Como funciona esse sistema?</strong>
                <p>Trata-se de uma licença anual de uso onde o cliente GOLD poderá desfrutar das cargas atuais e todos os próximos lançamentos imediatamente durante este período sem ter a preocupação em comprar mais nenhuma carga. No caso dos novos clientes que ainda não trabalham com a tecnologia de transponder, após adquirir o equipamento REMAP que mais se encaixa com seu orçamento, escolher o plano ideal, receberá as cargas atuais e os lançamentos sempre após 6 meses da data de implantação no site da GOLD  independente da data da adesão do plano.</p>
              </li>
              <li class="list-group-item">
                <strong>2. O que precisa para entrar no plano?</strong>
                <p>Cliente GOLD que já possui REMAP II, Light, Work 1, Work 2, Smart ou Smart III (portanto que possua o cartão de expansão) poderá procurar o seu vendedor GOLD adquirindo o plano G-Tech, formalizando pagamento após preenchimento e assinatura do termo de adesão.</p>
                <p>Cliente novos deverão escolher um dos equipamentos disponíveis para o Plano (REMAP SMART ou REMAP SMART III), alguns acessórios básicos para utilização das funções de acordo com suas necessidades e o plano escolhido que melhor atender.</p>
              </li>
              <li class="list-group-item">
                <strong>3. Como devo fazer para utilizar o máximo das funções da minha REMAP após aderir ao plano G-tech?</strong>
                <p>Consulte o seu vendedor quais cabos, conectores, soquetes e outros acessórios que são fundamentais para os novos softwares (cargas) que serão liberados em seu equipamento, assim que for confirmada a adesão ao plano G-tech.</p>
              </li>
              <li class="list-group-item">
                <strong>4. O que acontece com o cliente que não quiser aderir ao plano?</strong>
                <p>Continuará comprando as cargas individualmente, conforme o modelo de venda existente. O novo modelo (G-tech) não anula o modelo anterior.</p>
              </li>
              <li class="list-group-item">
                <strong>5. O que acontece com as cargas compradas antes da adesão do plano?</strong>
                <p>Essas cargas pertencem ao cliente e portanto continuaram no equipamento. Independentemente do tempo de permanência no plano G-tech. Então em caso desistência do plano G-tech o equipamento volta para a configuração anterior a adesão.</p>
              </li>
              <li class="list-group-item">
                <strong>6. Como faço para saber quais cargas já tenho no meu equipamento?</strong>
                <p>Ligue para o departamento técnico da Gold com o número de série de seu equipamento em mãos e solicite a listagem de suas cargas.</p>
